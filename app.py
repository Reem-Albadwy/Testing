    confidence = float(np.max(probs)) * 100
    st.success(f"ğŸ©º Predicted Disease: **{pred_class}**")
    st.info(f"ğŸ“Š Confidence: **{confidence:.2f}%**")

    # ğŸ”— Ø±ÙˆØ§Ø¨Ø· Ù„Ù„Ù…Ø¹Ù„ÙˆÙ…Ø§Øª
