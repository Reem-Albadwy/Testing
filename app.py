# ---------- Ø§Ø³Ù… Ø§Ù„Ù…Ø±ÙŠØ¶ ----------
patient_name = st.text_input("ğŸ‘¤ Enter patient name:")

# ---------- Ø±ÙØ¹ Ø§Ù„ØµÙˆØ±Ø© ----------
